class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to sort the array into a wave-like array.
    void convertToWave(vector<int>& arr, int n){
        vector<int> temp = arr;
        for(int i=2;i<=n;i+=2){
            temp[i-2]=arr[i-1];
            temp[i-1]=arr[i-2];
        }
        if(n%2==1){
            temp.push_back(arr[n-1]);
        }
        arr=temp;
    }
};

